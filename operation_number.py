a = 1
b = 2
c = 30
d = 4.0

tambah = a + b # operasi bilangan penjumlahan ( 1 + 2 )
kurang = c - b # operasi bilangan pengurangan ( 30 - 2 )
kali = c * b   # operasi bilangan perkalian ( 30 * 2 )
bagi = d / b   # operasi bilangan pembagian ( 4.0 / 2 )
sisa = c % b   # operasi bilangan sisa ( 30 % 2 )
pangkat = b ** b # operasi bilangan perpangkatan ( 2 ** 2 )

print(tambah) #menampilkan hasil dari operasi penjumlahan
print(kurang) #menampilkan hasil dari operasi pengurangan
print(kali)   #menampilkan hasil dari operasi perkalian
print(bagi)   #menampilkan hasil dari operasi pembagian
print(sisa)   #menampilkan hasil dari operasi pembagian yang memiliki sisa
print(pangkat)#menampilkan hasil dari operasi perpangkatan